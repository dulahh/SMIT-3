marks=int(input("Enter your marks in percentage:"))
if marks>=80:
    print("You've got A grade!")
elif marks>=70:
    print("You've got B grade!")
elif marks>=60:
    print("You've got C grade!")    
elif marks>=50:
    print("You've got D grade!")
elif marks>=40:
    print("You've got E grade!")
else:
    print("You've got F grade!")